fun TextStyle.nonLinearScale(density: Density, maxScale: Float): TextStyle {
    val scale = density.fontScale
    if (scale <= 1f) return this
    val cappedScale = scale.coerceAtMost(maxScale)
    val scaleRatio = cappedScale / scale
    return this.copy(
        fontSize = this.fontSize * scaleRatio,
        lineHeight = this.lineHeight * scaleRatio
    )
}

